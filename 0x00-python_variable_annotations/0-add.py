#!/usr/bin/env python3
"""
Contains a type-annoted function that that takes a float a and a float b as
arguments and returns their sum as a float.
"""


def add(a: float, b: float) -> float:
    """ Adds two floats
    Args:
        a(float): First number
        b(float): Second number
    Return:
        (float): Sum of two numbers
    """
    return a + b
